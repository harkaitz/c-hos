#include "hos/hos.h"

// Ascii art banner, "HOS" in big letters.
static const char *banner =
    " .----------------.  .----------------.  .----------------. "    "\n"
    "| .--------------. || .--------------. || .--------------. |"    "\n"
    "| |  ____  ____  | || |     ____     | || |    _______   | |"    "\n"
    "| | |_   ||   _| | || |   .'    `.   | || |   /  ___  |  | |"    "\n"
    "| |   | |__| |   | || |  /  .--.  \\  | || |  |  (__ \\_|  | |"  "\n"
    "| |   |  __  |   | || |  | |    | |  | || |   '.___`-.   | |"    "\n"
    "| |  _| |  | |_  | || |  \\  `--'  /  | || |  |`\\____) |  | |"  "\n"
    "| | |____||____| | || |   `.____.'   | || |  |_______.'  | |"    "\n"
    "| |              | || |              | || |              | |"    "\n"
    "| '--------------' || '--------------' || '--------------' |"    "\n"
    " '----------------'  '----------------'  '----------------' "    "\n"
    ;


static bool _banner(int argc, char *argv[], int *ret) {
    if (!strcmp(argv[0], "banner")) {
        hos_print(banner, true);
        return true;
    } else {
        return false;
    }
}

hos_Module hos_Banner_Module = {
    .Command = _banner
};
